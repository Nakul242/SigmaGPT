import "./ChatWindow.css";
import Chat from "./Chat";
import { useContext, useEffect } from "react";
import { MyContext } from "./MyContext";
import { ScaleLoader } from "react-spinners";
import { useState } from "react";

function ChatWindow() {
    const { prompt, setPrompt, reply, setReply, currThreadId, prevChats, setPrevChats, setNewChat } = useContext(MyContext);
    const [loading, setLoading] = useState(false);
    const [isOpen, setIsOpen] = useState(false);

    const getReply = async () => {
        // Function to get reply based on prompt and currThreadId
        if (!prompt.trim()) return; // Avoid empty prompts
        setLoading(true);
        setNewChat(false);

        const options = {
            method: 'POST',
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify({
                message: prompt,
                threadId: currThreadId
            })
        };

        try {
            const response = await fetch('http://localhost:8080/api/chat', options);
            const res = await response.json();
            console.log(res);
            setReply(res.reply);
        }
        catch (error) {
            console.error('Error fetching reply:', error);
            setLoading(false);
        }

    };

    // Append new chat to prevChats
    useEffect(() => {
        if ( prompt && reply ) {
            setPrevChats(prevChats => (
                [...prevChats, {
                    role: "user",
                    content: prompt
                },
                {
                    role: "assistant",
                    content: reply
                }]
            ));
        }
        setPrompt("");
        setLoading(false);

    }, [reply]);

    const handleProfileClick = () => {
        setIsOpen(!isOpen);
    };

    return (
        <div className="chatWindow">
            <div className="navbar">
                <span>SigmaGPT <i className="fa-solid fa-angle-down"></i></span>
                <div className="userIconDiv" onClick={handleProfileClick}>
                    <span className="userIcon"><i className="fa-solid fa-user"></i></span>
                </div>
            </div>

            {
                isOpen &&
                <div className="dropDown">
                    <div className="dropDownItem"> <i class="fa-solid fa-gear"></i> Settings</div>
                    <div className="dropDownItem"> <i class="fa-solid fa-cloud-arrow-up"></i> Upgrade plan</div>
                    <div className="dropDownItem"> <i class="fa-solid fa-arrow-right-from-bracket"></i> Log out</div>
                </div>
            }

            <Chat />
            <ScaleLoader color={"#fff"} loading={loading}></ScaleLoader>

            <div className="chatInput">
                <div className="inputBox">
                    <input type="text" placeholder="Ask anything" 
                        value={prompt}
                        onChange={(e) => setPrompt(e.target.value)}
                        onKeyDown={(e) => e.key === 'Enter'? getReply() : ''}
                    />

                    <div id="submit" onClick={getReply}><i className="fa-solid fa-paper-plane"></i></div>
                </div>
                <p className="info">
                    SigmaGPT can make mistakes. Check importand info. See Cookie Preferences.
                </p>
            </div>
        </div>
    );
}

export default ChatWindow;